def build_simpler():
    data_list = JSONFile(os.path.join('data', 'countries.json')).read()
    d_list = []
    for d in data_list:
        is_un_member = d['unMember']
        if not is_un_member:
            continue
        d_simpler = dict(
            alpha3=d['cca3'],
            common_name=d['name']['common'],
            capital_latlng=d['latlng'],
            color=get_color(d['cca3']),
        )
        d_list.append(d_simpler)
    d_list = sorted(d_list, key=lambda d: d['alpha3'])

    simpler_path = os.path.join('data', 'countries.simpler.json')
    JSONFile(simpler_path).write(d_list)
    log.info(f'Wrote {len(d_list)} countries to {simpler_path}')


if __name__ == '__main__':
    build_simpler()
